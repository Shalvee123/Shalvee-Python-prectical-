#Start with a list of 3 colors. Use .append() to add a fourth and .insert() to add one at the second position.
# List of 3 colors
colors = ["Red", "Blue", "Green"]

# Add a fourth color using append()
colors.append("Yellow")

# Add a color at the second position (index 1) using insert()
colors.insert(1, "Purple")

# Print final list
print(colors)
